<?php
// Problem 1: Addition of two numbers
function addNumbers($num1, $num2) {
    return $num1 + $num2;
}

// Test the function with example inputs
$sum = addNumbers(3, 4);
echo "The sum is: $sum"; // Output: The sum is: 7

// Problem 2: Multiplication of two numbers
function multiplyNumbers($num1, $num2) {
    return $num1 * $num2;
}

// Test the function with example inputs
$product = multiplyNumbers(3, 4);
echo "The product is: $product"; // Output: The product is: 12

// Problem 3: Calculate the area of a triangle given its base and height
function calculateAreaTriangle($base, $height) {
    return 0.5 * $base * $height;
}

// Test the function with example inputs
$area = calculateAreaTriangle(4, 6);
echo "The area is: $area"; // Output: The area is: 24

// Problem 4: Find the square root of a number
function sqrt($number) {
    return sqrt($number);
}

// Test the function with an example input
$sqrtRoot = sqrt(16);
echo "The square root is: $sqrtRoot"; // Output: The square root is: 4

?>
